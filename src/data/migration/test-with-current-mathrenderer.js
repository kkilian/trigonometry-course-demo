    testResults.summary.avgIssuesPerProblem = testResults.totalIssues / testResults.tested;
    testResults.summary.commonIssues = Object.entries(testResults.issueTypes)
        .sort(([,a], [,b]) => b - a)
        .slice(0, 5)
        .map(([issue, count]) => ({ issue, count }));
    
